package com.demo;

public class Employee {
	private int salary;
	private int id;



	

public Employee(int salary,int id) {
	this.salary=salary;
	this.id=id;
		
	}

public Employee() {


}

public void setSalary(int salary) {
	this.salary=salary;
	
}
public int getSalary() {
	return salary;
}
public void setId(int Id) {
	this.id=Id;
}
public int getId() {
	return id;
}



}


	


